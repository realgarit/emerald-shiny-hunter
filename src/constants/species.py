# Reverse mapping: Internal ID to National Dex
INTERNAL_TO_NATIONAL = {internal: national for national, internal in NATIONAL_DEX.items()}


def get_national_dex(internal_id: int) -> int:
    """
    Convert internal species ID to National Dex number.

    Args:
        internal_id: The internal species ID used in memory

    Returns:
        National Dex number, or 0 if not found
    """
    return INTERNAL_TO_NATIONAL.get(internal_id, 0)


def get_internal_id(national_dex: int) -> int:
    """
    Convert National Dex number to internal species ID.

    Args:
        national_dex: The National Pokedex number (1-386)

    Returns:
        Internal species ID, or 0 if not found
    """
    return NATIONAL_DEX.get(national_dex, SPECIES_NONE)


def species_from_national_dex(national_dex: int) -> tuple:
    """
    Get species info from National Dex number.

    Args:
        national_dex: The National Pokedex number (1-386)

    Returns:
        Tuple of (internal_id, name) or (0, "Unknown") if not found
    """
    internal_id = NATIONAL_DEX.get(national_dex, SPECIES_NONE)
    if internal_id == SPECIES_NONE:
        return 0, f"Unknown (Dex #{national_dex})"
    name = SPECIES_NAMES.get(internal_id, f"Pokemon #{national_dex}")
    return internal_id, name


