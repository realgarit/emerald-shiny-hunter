# Name to ID mapping (case-insensitive lookup)
SPECIES_BY_NAME = {name.lower(): species_id for species_id, name in SPECIES_NAMES.items()}


def get_species_name(species_id: int) -> str:
    """Get the name of a Pokemon by its species ID."""
    return SPECIES_NAMES.get(species_id, f"Unknown({species_id})")


def get_species_id(name: str) -> int:
    """Get the species ID by name (case-insensitive)."""
    return SPECIES_BY_NAME.get(name.lower(), SPECIES_NONE)


